let container = document.getElementById("container");

// ================= DISPLAY =================
function displayItems(list){
  container.innerHTML = "";
  list.forEach(item=>{
    container.innerHTML += `
      <div class="card" onclick="showDetails('${item.name[currentLanguage]}','${item.vitamin[currentLanguage]}','${item.details[currentLanguage]}')">
        <h2>${item.emoji} ${item.name[currentLanguage]}</h2>
        <p><b>Vitamins:</b> ${item.vitamin[currentLanguage]}</p>
        <small>${item.category[currentLanguage]}</small>
      </div>
    `;
  });
}

// ================= SEARCH =================
function searchItem(){
  let value = document.getElementById("search").value.toLowerCase();

  let filtered = data.filter(item => {
    return Object.values(item.name).some(v => v.toLowerCase().includes(value)) ||
           Object.values(item.vitamin).some(v => v.toLowerCase().includes(value)) ||
           Object.values(item.details).some(v => v.toLowerCase().includes(value));
  });

  displayItems(filtered);
}

// ================= FILTER =================
function filterCategory(buttonText){
  if(buttonText === translations["en"].all){ 
    displayItems(data); 
    return; 
  }
  let filtered = data.filter(item => item.category["en"] === buttonText);
  displayItems(filtered);
}

// ================= MODAL =================
function showDetails(name, vitamin, details){
  document.getElementById("modalTitle").innerText = name;
  document.getElementById("modalText").innerText = `Vitamins: ${vitamin}\n\n${details}`;
  document.getElementById("modal").style.display = "flex";
}

function closeModal(){
  document.getElementById("modal").style.display = "none";
}

// ================= DARK MODE =================
function toggleDarkMode(){
  document.body.classList.toggle("dark");
}

// ================= LANGUAGE SWITCH =================
function changeLanguage(){
  currentLanguage = document.getElementById("languageSelect").value;
  let t = translations[currentLanguage];

  document.getElementById("appTitle").innerText = t.title;
  document.getElementById("search").placeholder = t.search;

  let buttons = document.querySelectorAll(".filter-buttons button");
  buttons[0].innerText = t.all;
  buttons[1].innerText = t.fruit;
  buttons[2].innerText = t.vegetable;
  buttons[3].innerText = t.nut;
  buttons[4].innerText = t.meat;
  buttons[5].innerText = t.herb;
  buttons[6].innerText = t.grain;

  displayItems(data);
}

